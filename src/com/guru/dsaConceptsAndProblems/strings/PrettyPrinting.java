package com.guru.dsaConceptsAndProblems.strings;

public class PrettyPrinting {

    public static void main(String[] args) {

        float f = 453.1274f;
        System.out.printf("Formatted number is %.2f ", f);

        System.out.println(); // for new line

        System.out.printf("PI value %.3f", Math.PI);

        System.out.println(); // for new line

        System.out.printf("Hello my name is %s and I am %s", "Guru", "Cool");
    }
}
